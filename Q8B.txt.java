// Q8B  Print even numbers from 1 to 10 using while loop

public class Even1 {
    public static void main(String[] args) {
		
        System.out.println("Even Numbers");
        
            int i = 1;
            
        while(i <= 10) {
                
        if(i % 2 == 0) {
        
            System.out.println(i);
                }
        i++;
            }
        }
    
}


